/* Задание 3
Используя Math.random() вам необходимо генерировать цифры от 0 до 9, и создать массив состоящий из 5 таких элементов
1. Рассчитать сумму элементов этого массива
2. Найти минимальное число
3. Найти есть ли в этом массиве число 3
*/

// Генерируем массив из 5 случайных чисел от 0 до 9
let randomArray = Array.from({length: 5}, () => Math.floor(Math.random() * 10));

// Выводим сгенерированный массив
console.log("Сгенерированный массив:", randomArray);

// Рассчитываем сумму элементов массива
let sum = randomArray.reduce((acc, curr) => acc + curr, 0);
console.log("Сумма элементов массива:", sum);

// Находим минимальное число в массиве
let minNumber = Math.min(...randomArray);
console.log("Минимальное число в массиве:", minNumber);

// Проверяем, есть ли число 3 в массиве
let hasNumberThree = randomArray.includes(3);
console.log("Число 3 есть в массиве:", hasNumberThree);



